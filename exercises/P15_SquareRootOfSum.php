<?php

class P15_SquareRootOfSum {
    public function main(): void {
        // Define two numbers
        $numberA = 70;
        $numberB = 11;


        $sum = $numberA + $numberB;

        // Calculate the square root of the sum
        $squareRoot = sqrt($sum);

        // Output the result
        echo $squareRoot . "\n";    }
}
